    private void BuscarPessoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarPessoaActionPerformed
        DigitalPersistencia di = null;
        di = new DigitalPersistencia();
        byte[] b;

        CadDigital caddig = new CadDigital();
        CadPessoa pe = new CadPessoa();
        Integer id;
        id = Integer.parseInt(buscaid.getText());
        caddig = di.BuscaDigitalALL(id);
        CapturaDigital mer = new CapturaDigital();
        mer.setTemplates(mer.Injetar_Digitais_Pessoa(caddig));
        // se retornar com digitais ja inseri para alterar
        CadastrodeDigital sa = new CadastrodeDigital();
        pe = di.BuscaPessoa(id);
        sa.setTemplates(mer.getTemplates());
        sa.UpdateUIP();
        sa.setPe(pe);
        sa.setAlterar(true);
        sa.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_BuscarPessoaActionPerformed

    private void salvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvar1ActionPerformed
        if (getMe() == null) {
            JOptionPane.showMessageDialog(rootPane, "Voce ainda não Cadastrou Ditais");

        } else {

            DigitalPersistencia di = new DigitalPersistencia();
            di.setCapturadigital(getMe());
            Integer idd;
            idd = Integer.parseInt(buscaid1.getText());
            CadPessoa pe = new CadPessoa(idd, "Clayton Ferraz");
            CadDigital cad = new CadDigital(pe);
            di.setCaddigital(cad);
            di.setLogin(pe);
            di.ExtrairDigitais();
            String b = di.CadastraDigitalTeste();
            JOptionPane.showMessageDialog(rootPane, b);

        }
    }//GEN-LAST:event_salvar1ActionPerformed

