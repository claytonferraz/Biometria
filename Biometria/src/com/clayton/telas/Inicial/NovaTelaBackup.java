    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        // TODO add your handling code here:

        if (me != null) {
            CadastrodeDigital sa = new CadastrodeDigital();
            sa.setTemplates(me.getTemplates());
            sa.UpdateUIP();
            boolean cadastro = true;
            sa.setCadastro(cadastro);
            sa.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Este Usuario, não possui Digitais  Cadastradas.\n"
                    + "Por favor, clique em Cadastrar Digitais ou pressione C do teclado");
        }
    }//GEN-LAST:event_jMenuItem5ActionPerformed

